public class Main {
    public static void main(String[] args)
    {
        for(int c = 0; c <= 10; c++){
            System.out.print(c + " ");
        }

        System.out.println("\n");

        for(int c = 10; c >= 0; c--){
            System.out.print(c + " ");
        }

        System.out.println("\n");

        for(int c = 0; c <= 10; c++){
            System.out.print(c + " ");
        }
        System.out.println("\n");

        for(int d = 0; d <= 18; d+=3){ //d = d + 3
            System.out.print(d + " ");
        }
        System.out.println("\n");

        for(int d = 18; d >= 0; d-=3){
            System.out.print(d + " ");
        }
        System.out.println("\n");
    }
}